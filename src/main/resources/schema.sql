    drop table if exists author CASCADE;
    drop table if exists book CASCADE;
    drop table if exists book_genre CASCADE;
    drop table if exists comment CASCADE;
    drop table if exists genre CASCADE;


    create table author (
        id_author bigint generated by default as identity,
        author_name varchar(255) not null,
        year_author Date,
        unique (author_name),
        primary key (id_author)
    );

    create table book (
        id_book bigint generated by default as identity,
        name_book varchar(255),
        date_book Date not null,
        id_author bigint,
        foreign key (id_author) references author(id_author) on delete cascade,
        primary key (id_book)
    );

     create table genre(
        id_genre bigint generated by default as identity,
        name_genre varchar(255) not null,
        unique (name_genre),
        primary key (id_genre)
    );

    create table book_genre (
        id_book bigint not null,
        id_genre bigint not null,
        foreign key (id_genre) references genre(id_genre) on delete cascade,
        foreign key (id_book) references book(id_book) on delete cascade
    );

    create table comment (
        id_comment bigint generated by default as identity,
        text varchar(255),
        id_book bigint,
        primary key (id_comment),
        foreign key (id_book) references book(id_book) on delete cascade
    );

